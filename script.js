function openNavLeft() {
    document.getElementById("mySidebarLeft").style.width = "450px";
    document.getElementById("main").style.marginLeft = "450px";
  }
  
  function closeNavLeft() {
    document.getElementById("mySidebarLeft").style.width = "0";
    document.getElementById("main").style.marginLeft= "0";
  }
  function openNavRight() {
    document.getElementById("mySidebarRight").style.width = "450px";
    document.getElementById("main").style.marginRight = "450px";
  }
  
  function closeNavRight() {
    document.getElementById("mySidebarRight").style.width = "0";
    document.getElementById("main").style.marginRight= "0";
  }